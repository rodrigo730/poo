    private void btnCalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalActionPerformed
        // TODO add your handling code here:
        int valor = Integer.parseInt(txtValor.getValue().toString());
        int rd = (valor % 2);
        Double Elevadoaocubo = Math.pow(valor, 3);
        Double raizquadrada = Math.sqrt(valor);
        Double raizcubica = Math.cbrt(valor);
        int absoluto = Math.abs(valor);
        
        
        lblM1.setText(Integer.toString(rd));
        lblM2.setText(Double.toString(Elevadoaocubo));
        lblM3.setText(Double.toString(raizquadrada));
        lblM4.setText(Double.toString(raizcubica));
        lblM5.setText(Integer.toString(absoluto));
        painel.setVisible(true);
    }//GEN-LAST:event_btnCalActionPerformed

